  /// Set by locale_provider when the selected language is not supported by
  /// GlobalMaterialLocalizations (e.g. 'ceb'), so _t() still returns the
  /// correct translations even when the Flutter locale is remapped to a
  /// supported fallback.
  static String? _langOverride;
  static void setLangOverride(String? code) => _langOverride = code;

  String _t(String key) {
    final code = _langOverride ?? locale.languageCode;
    return (_data[code] ?? _data['en']!)[key] ?? _data['en']![key]!;
  }

  /// Template substitution: _tf('hello {name}', {'name': 'Ana'}) → 'hello Ana'
  String _sub(String key, Map<String, String> args) {
    String v = _t(key);
    args.forEach((k, val) => v = v.replaceAll('{$k}', val));
    return v;
  }

